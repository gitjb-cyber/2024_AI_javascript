
// 문자열(String) - "", '', ``(backtick)
const number = "1";
const number1 = '1';
const number2 = `1`;
console.log(number + number1 + number2); // 111

// 숫자(Number)
const age = 50;
const age1 = 20;
console.log(age + age1); // 70

// Number화 시키기
const a = Number("100"); // 100
const b = Number("200"); // 200
console.log(a+b);

const c = Number("10"); // 10
const d = Number("50"); // 50
console.log(c+d);

// String화 시키기
const e = String(100); // "100"
const f = String(4); // "4"
console.log(e+f) // 1004